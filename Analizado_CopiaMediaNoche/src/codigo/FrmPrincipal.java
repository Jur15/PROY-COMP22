      private void analizarLexico() throws IOException{
        int cont = 1;
        
        String expr = (String) txtResultado.getText();
        Lexer lexer = new Lexer(new StringReader(expr));
        String resultado = "Line " + cont + "\t\tEncontrado\n";
        while (true) {
            Tokens token = lexer.yylex();
            if (token == null) {
                txtAnalizarLex.setText(resultado);
                return;
            }
            switch (token) {
                case Linea:
                    cont++;
                    resultado += "Line --- " + cont + "\n";
                    break;
                case Comillas:
                    resultado += "  <Comillas>\t\t" + lexer.lexeme + "\n";
                    break;
                case Cadena:
                    resultado += "  <Tipo de dato>\t" + lexer.lexeme + "\n";
                    break;
                case Return:
                    resultado += "  <Palabra Reservada>\t" + lexer.lexeme + "\n";
                    break;
                case If:
                    resultado += "  <Condicional>\t" + lexer.lexeme + "\n";
                    break;
                case Else:
                    resultado += "  <Reservada else>\t" + lexer.lexeme + "\n";
                    break; 
                case While:
                    resultado += "  <Reservada while>\t" + lexer.lexeme + "\n";
                    break;
               
                case Igual:
                    resultado += "  <Operador igual>\t" + lexer.lexeme + "\n";
                    break;
             
                case Suma:
                    resultado += "  <Operador suma>\t" + lexer.lexeme + "\n";
                    break;
                case Resta:
                    resultado += "  <Operador resta>\t" + lexer.lexeme + "\n";
                    break;
                case Multiplicacion:
                    resultado += "  <Operador multiplicacion>\t" + lexer.lexeme + "\n";
                    break;
                case Division:
                    resultado += "  <Operador division>\t" + lexer.lexeme + "\n";
                    break;
                case Op_logico:
                    resultado += "  <Operador logico>\t" + lexer.lexeme + "\n";
                    break;
                case Op_incremento:
                    resultado += "  <Operador incremento>\t" + lexer.lexeme + "\n";
                    break;
                case Op_relacional:
                    resultado += "  <Operador relacional>\t" + lexer.lexeme + "\n";
                    break;
                case Op_atribucion:
                    resultado += "  <Operador atribucion>\t" + lexer.lexeme + "\n";
                    break;
                case Op_booleano:
                    resultado += "  <Operador booleano>\t" + lexer.lexeme + "\n";
                    break;
                case Parentesis_a:
                    resultado += "  <Parentesis de apertura>\t" + lexer.lexeme + "\n";
                    break;
                case Parentesis_c:
                    resultado += "  <Parentesis de cierre>\t" + lexer.lexeme + "\n";
                    break;
                case Llave_a:
                    resultado += "  <Llave de apertura>\t" + lexer.lexeme + "\n";
                    break;
                case Llave_c:
                    resultado += "  <Llave de cierre>\t" + lexer.lexeme + "\n";
                    break;
                case Corchete_a:
                    resultado += "  <Corchete de apertura>\t" + lexer.lexeme + "\n";
                    break;
                case Corchete_c:
                    resultado += "  <Corchete de cierre>\t" + lexer.lexeme + "\n";
                    break;
                case Main:
                    resultado += "  <Reservada main>\t" + lexer.lexeme + "\n";
                    break;
                case P_coma:
                    resultado += "  <Caracter numeral>\t" + lexer.lexeme + "\n";
                    break;
                case Identificador:
                    resultado += "  <Identificador>\t\t" + lexer.lexeme + "\n";
                    break;
                case Numero:
                    resultado += "  <Numero>\t\t" + lexer.lexeme + "\n";
                    break;
                case G_Bajo:
                    resultado += "  <Guion Bajo>\t\t" + lexer.lexeme + "\n";
                    break;  
                    
                case Switch:
                    resultado += "  <Condicional >\t\t" + lexer.lexeme + "\n";
                    break;  
                case True:
                    resultado += "  <Valor Booleano>\t\t" + lexer.lexeme + "\n";
                    break;  
                case False:
                    resultado += "  <Valor Booleano>\t\t" + lexer.lexeme + "\n";
                    break;  
                case Case:
                    resultado += "  <Palabra reservada de condicional>\t\t" + lexer.lexeme + "\n";
                    break;  
                case Default:
                    resultado += "  <Palabra Reservada>\t\t" + lexer.lexeme + "\n";
                    break;  
                case Punto:
                    resultado += "  <Caracter >\t\t" + lexer.lexeme + "\n";
                    break;  
                case Break:
                    resultado += "  <Palabra reservada>\t\t" + lexer.lexeme + "\n";
                    break;  
                case Null:
                    resultado += "  < Valor >\t\t" + lexer.lexeme + "\n";
                    break;  
                case Type_Int:
                    resultado += "  <Tipo de dato>\t\t" + lexer.lexeme + "\n";
                    break;  
                
                case D_Punto:
                    resultado += "  <Tipo de dato>\t\t" + lexer.lexeme + "\n";
                    break;  
               
                case S_Dolar:
                    resultado += "  <Signo Dolar>\t\t" + lexer.lexeme + "\n";
                    break;  
                case ERROR:
                    resultado += " ";
                    break;
                default:
                    resultado += "  <-----------> " + lexer.lexeme + " >\n";
                    break;
            }
        }
    }

   
